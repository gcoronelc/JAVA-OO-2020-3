    private void BtnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnCalcularActionPerformed
        
        double n1=Double.parseDouble(Tnota1.getText());
        double n2=Double.parseDouble(Tnota2.getText());
        double n3=Double.parseDouble(Tnota3.getText());
        double n4=Double.parseDouble(Tnota4.getText());
        double n5=Double.parseDouble(Tnota5.getText());

        double mejor = 20;
        double prom = 20;
        boolean apro = true;
        PromedioAlumno PAlu;
        
        PAlu = new PromedioAlumno();
        
        mejor = PAlu.NotaMayor(n1, n2, n3, n4, n5);
        prom = PAlu.NotaPromedio(n1, n2, n3, n4, n5);
        apro = PAlu.Aprobado(prom);
 
        Tmejor.setText("Mejor nota: " + mejor);
        Tprom.setText("Promedio: " + prom);
        if (apro) {
            Tcondi.setText("Aprobado");
        } else {
            Tcondi.setText("Jalado");
        }
        
    }//GEN-LAST:event_BtnCalcularActionPerformed

