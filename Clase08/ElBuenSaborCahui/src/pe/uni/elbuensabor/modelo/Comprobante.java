    private double total;
    private double servicio;
    private double total_general;
    
    //constructor
    public Comprobante(){

    } 
    
        
    //set y get
       
    public double getTotal() {
        return total;
    }

    public void setTotal(double total) {
        this.total = total;
    }

    public double getServicio() {
        return servicio;
    }

    public void setServicio(double servicio) {
        this.servicio = servicio;
    }

    public double getTotal_general() {
        return total_general;
    }

    public void setTotal_general(double total_general) {
        this.total_general = total_general;
    }


    
